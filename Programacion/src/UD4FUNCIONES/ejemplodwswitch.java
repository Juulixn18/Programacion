package UD4FUNCIONES;

public class ejemplodwswitch {

	public static void main(String[] args) {
		char opcion = '2';
		switch (opcion) {
		case '1':
			System.out.println("Caso 1");
			break;

		case '2':
			System.out.println("Caso 2");
			break;
		default:
			System.out.println("Opcion no existe");
		}

	}
}
